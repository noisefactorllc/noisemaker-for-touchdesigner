def expand_vectors(index):
    """Five expanded vec3 uniforms (insertion-ordered) or None when out of range [1,55]."""
    idx = int(index)
    if idx <= 0 or idx > 55:
        return None
    amp, freq, offset, phase, _mode = _PALETTES[idx - 1]
    return [
        ('paletteOffset', list(offset)),
        ('paletteAmp', list(amp)),
        ('paletteFreq', list(freq)),
        ('palettePhase', list(phase)),
    ]


def expand_mode(index):
    idx = int(index)
    if idx <= 0 or idx > 55:
        return None
    return _PALETTES[idx - 1][4]
